package model;

public class Kamar {
    private int id_kamar;
    private int nomor_kamar;
    private String jenis_kamar;
    private String fasilitas;
    private int harga;
    private String status;
    private int lantai_kamar;
    private int terisi;
    private int kapasitas;

    public int getId_kamar() {
        return id_kamar;
    }

    public void setId_kamar(int id_kamar) {
        this.id_kamar = id_kamar;
    }

    public int getNomor_kamar() {
        return nomor_kamar;
    }

    public void setNomor_kamar(int nomor_kamar) {
        this.nomor_kamar = nomor_kamar;
    }

    public String getJenis_kamar() {
        return jenis_kamar;
    }

    public void setJenis_kamar(String jenis_kamar) {
        this.jenis_kamar = jenis_kamar;
    }

    public String getFasilitas() {
        return fasilitas;
    }

    public void setFasilitas(String fasilitas) {
        this.fasilitas = fasilitas;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getLantai_kamar() {
        return lantai_kamar;
    }

    public void setLantai_kamar(int lantai_kamar) {
        this.lantai_kamar = lantai_kamar;
    }

    public int getTerisi() {
        return terisi;
    }

    public void setTerisi(int terisi) {
        this.terisi = terisi;
    }

    public int getKapasitas() {
        return kapasitas;
    }

    public void setKapasitas(int kapasitas) {
        this.kapasitas = kapasitas;
    }
    
    
}
