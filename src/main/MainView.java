    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        int kolom = JTabelKamar.getSelectedRow();
        if(Integer.parseInt(JTabelKamar.getValueAt(kolom, 7).toString()) > Integer.parseInt(JTabelKamar.getValueAt(kolom, 8).toString())){
            JOptionPane.showMessageDialog(this, "Kapasitas Kamar sudah penuh, Silahkan pilih kamar lain", "Kamar Penuh", JOptionPane.ERROR_MESSAGE);
        } else if (getjTextIdPasien().getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih Dahulu data yang mau diubah dengan klik salah satu data di tabel", "Input Tidak Lengkap", JOptionPane.ERROR_MESSAGE);
        } else {
            dc.update();
            dc.isiTabel();
            zz.updateKamar();
            zz.isiTabelKamar();
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        if (getTemp().getText().isEmpty() && getTemp2().getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih Dahulu data yang mau dihapus dengan klik salah satu data di tabel", "Data Tidak Lengkap", JOptionPane.ERROR_MESSAGE);
        } else {
            dc.delete();
            dc.isiTabel(); 
            zz.deletekamar();
            zz.isiTabelKamar();
        }
    }//GEN-LAST:event_btnhapusActionPerformed

