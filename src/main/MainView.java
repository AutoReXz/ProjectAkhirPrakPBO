    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed

        if (getjTextKamar().getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih Kamar terlebih dahulu dengan mengeklik salah satu kamar di dalam tabel", "Input Tidak Lengkap", JOptionPane.ERROR_MESSAGE);
        }else {
            int kolom = JTabelKamar.getSelectedRow();
            if(Integer.parseInt(JTabelKamar.getValueAt(kolom, 7).toString()) > Integer.parseInt(JTabelKamar.getValueAt(kolom, 8).toString())){
                JOptionPane.showMessageDialog(this, "Kapasitas Kamar sudah penuh, Silahkan pilih kamar lain", "Kamar Penuh", JOptionPane.ERROR_MESSAGE);
            } else {
                dc.insert();
                dc.isiTabel();
                zz.updateKamar();
                zz.isiTabelKamar();
            }
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

